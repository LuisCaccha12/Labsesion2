package sesion.pkg2.newpackage;

/**
 *
 * @author Estudiante
 */
public class Nave {

    private int nivel;
    private int puntaje;
    private int vidas;
    
    
    public Nave (){}
    
    public Nave(int nivel, int puntaje, int vidas) {
        this.nivel = nivel;
        this.puntaje = puntaje;
        this.vidas = vidas;
    }
    /**
     * @return the nivel
     */
    public int getNivel() {
        return nivel;
    }

    /**
     * @param nivel the nivel to set
     */
    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    /**
     * @return the puntaje
     */
    public int getPuntaje() {
        return puntaje;
    }

    /**
     * @param puntaje the puntaje to set
     */
    public void setPuntaje(int puntaje) {
        this.puntaje = puntaje;
    }

    /**
     * @return the vidas
     */
    public int getVidas() {
        return vidas;
    }

    /**
     * @param vidas the vidas to set
     */
    public void setVidas(int vidas) {
        this.vidas = vidas;
    }
    
}
